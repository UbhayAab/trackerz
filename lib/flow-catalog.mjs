export function getFlowsByDomain(domain) {
  return domain === "all" ? userFlows : userFlows.filter((item) => item.domain === domain);
}

export function getFlowStats() {
  return userFlows.reduce(
    (stats, item) => {
      stats.total += 1;
      stats.byDomain[item.domain] = (stats.byDomain[item.domain] || 0) + 1;
      return stats;
    },
    { total: 0, byDomain: {} },
  );
}

export function validateFlowCatalog(flows = userFlows) {
  const required = ["id", "domain", "title", "trigger", "inputs", "aiSteps", "outputs", "safeguards", "examples"];
  const ids = new Set();
  const errors = [];

  for (const item of flows) {
    for (const field of required) {
      if (!item[field] || (Array.isArray(item[field]) && item[field].length === 0)) {
        errors.push(`${item.id || "unknown"} missing ${field}`);
      }
    }

    if (ids.has(item.id)) {
      errors.push(`duplicate id ${item.id}`);
    }
    ids.add(item.id);

    if (!item.safeguards.some((guard) => /undo|review|confidence|audit|duplicate|raw|rls|delete|policy|source|auth|cap|sanity|warning|exact|formula|provider|inject|ignore|version|cross-user|invite|diagnosis|medical|false-positive|correction|hidden writes|balance/i.test(guard))) {
      errors.push(`${item.id} has weak safeguards`);
    }
  }

  return errors;
}
